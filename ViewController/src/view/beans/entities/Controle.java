package view.beans.entities;


public class Controle {
    private Long id;
    private Integer pourcentCntrole;
    private Number note;
    
    public Controle() {
        super();
    }

    public Controle(Long id, Integer pourcentCntrole, Number note) {
        this.id = id;
        this.pourcentCntrole = pourcentCntrole;
        this.note = note;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setPourcentCntrole(Integer pourcentCntrole) {
        this.pourcentCntrole = pourcentCntrole;
    }

    public Integer getPourcentCntrole() {
        return pourcentCntrole;
    }

    public void setNote(Number note) {
        this.note = note;
    }

    public Number getNote() {
        return note;
    }
}
