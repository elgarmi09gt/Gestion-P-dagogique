package view.beans.entities;

public class EtudiantBis {
    
    private String Numero;
    private String PrenomNom;
    private String IdEtudiant;
    private String InscriptionPed;
    
    public EtudiantBis() {
        super();
    }

    public EtudiantBis(String IdEtudiant, String Numero, String PrenomNom, String InscriptionPed) {
        this.Numero = Numero;
        this.PrenomNom = PrenomNom;
        this.IdEtudiant = IdEtudiant;
        this.InscriptionPed = InscriptionPed;
    }

    public void setNumero(String Numero) {
        this.Numero = Numero;
    }

    public String getNumero() {
        return Numero;
    }

    public void setPrenomNom(String PrenomNom) {
        this.PrenomNom = PrenomNom;
    }

    public String getPrenomNom() {
        return PrenomNom;
    }

    public void setIdEtudiant(String IdEtudiant) {
        this.IdEtudiant = IdEtudiant;
    }

    public String getIdEtudiant() {
        return IdEtudiant;
    }

    public void setInscriptionPed(String InscriptionPed) {
        this.InscriptionPed = InscriptionPed;
    }

    public String getInscriptionPed() {
        return InscriptionPed;
    }
}
