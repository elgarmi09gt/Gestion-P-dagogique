package view.beans.entities;

public class FiliereUE {
    private Long id;
    private Integer coef;
    private FilEcRepecheUe filec;
    
    public FiliereUE() {
        super();
    }

    public FiliereUE(Long id, Integer coef, FilEcRepecheUe filec) {
        this.id = id;
        this.coef = coef;
        this.filec = filec;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setCoef(Integer coef) {
        this.coef = coef;
    }

    public Integer getCoef() {
        return coef;
    }

    public void setFilec(FilEcRepecheUe filec) {
        this.filec = filec;
    }

    public FilEcRepecheUe getFilec() {
        return filec;
    }
}
